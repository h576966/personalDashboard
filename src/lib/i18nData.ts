export type AppLanguage = "en" | "no" | "sv";

export const LANGUAGE_OPTIONS: Array<{ value: AppLanguage; label: string }> = [
  { value: "en", label: "English" },
  { value: "no", label: "Norwegian" },
  { value: "sv", label: "Swedish" },
];

export const LOCALE_BY_LANGUAGE: Record<AppLanguage, string> = {
  en: "en",
  no: "nb-NO",
  sv: "sv-SE",
};

export function normalizeAppLanguage(value: unknown): AppLanguage {
  return value === "no" || value === "sv" ? value : "en";
}

export function formatShortDate(value: string | undefined, language: AppLanguage): string {
  if (!value) return "";

  try {
    return new Intl.DateTimeFormat(LOCALE_BY_LANGUAGE[language], {
      month: "short",
      day: "numeric",
    }).format(new Date(value));
  } catch {
    return "";
  }
}

export function formatShortTime(value: string, language: AppLanguage): string {
  try {
    return new Intl.DateTimeFormat(LOCALE_BY_LANGUAGE[language], {
      hour: "2-digit",
      minute: "2-digit",
    }).format(new Date(value));
  } catch {
    return "";
  }
}

